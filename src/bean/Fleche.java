package bean;

public class Fleche {
	private String arg;
	private String url;
	
	public Fleche(String arg, String url) {
	
		this.arg = arg;
		this.url = url;
	}

	
	public String getArg() {
		return arg;
	}

	public void setArg(String arg) {
		this.arg = arg;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
