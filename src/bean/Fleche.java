package bean;

public class Fleche {
	private String top;
	private String left;
	private String url;
	
	public Fleche(String top, String left, String url) {
	
		this.top = top;
		this.left = left;
		this.url = url;
	}

	public String getTop() {
		return top;
	}

	public void setTop(String top) {
		this.top = top;
	}

	public String getLeft() {
		return left;
	}

	public void setLeft(String left) {
		this.left = left;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
